const x = Math.floor(Math.random() * 100 + 1);
const y = Math.floor(Math.random() * 50 + 1);

// Sum 
const sumOutput = `${x} + ${y} = ${x+y}`;
console.log(sumOutput);

// Difference 
const differenceOutput = `${x} - ${y} = ${x-y}`;
console.log(differenceOutput);

// Product 
const productOuput = `${x} * ${y} = ${x*y}`;
console.log(productOuput);

// Quotient 
const quoitentOutput = `${x} / ${y} = ${x/y}`;
console.log(quoitentOutput);

// Remainder
const remainderOutput = `${x} % ${y} = ${x%y}`;
console.log(remainderOutput);